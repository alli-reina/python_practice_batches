# Program: Raise the First Number to the Power of the Second Number
# This program asks the user for two numbers and prints the result when the first number is raised to the power of the second number

# Ask the user for two numbers
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Calculate num1 raised to the power of num2
result = num1 ** num2

# Print the result
print("Result:", result)
