# Program Name: Check if Two Numbers Are Equal
# This program asks the user to input two numbers and checks if they are equal
# If the numbers are equal, it prints "Equal", otherwise it prints "Not Equal"

# Ask the user for two numbers
num1 = int(input("Enter 1st number: "))
num2 = int(input("Enter 2nd number: "))

# Compare the two numbers and print the result
if num1 == num2:
    print("Equal")
