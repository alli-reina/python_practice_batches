# Program: Check if the Numbers are Not Equal
# This program asks the user to input two numbers and prints "Not Equal" if the numbers are different

# Ask the user for two numbers
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Check if the numbers are not equal and print "Not Equal" if true
if num1 != num2:
    print("Not Equal")
