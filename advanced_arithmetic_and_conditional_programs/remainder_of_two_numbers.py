# Program: Print the Remainder of Two Numbers
# This program asks the user to input two numbers and prints the remainder when the first number is divided by the second

# Ask the user for two numbers
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Calculate the remainder and print the result
remainder = num1 % num2
print("Remainder:", remainder)
